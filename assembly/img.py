shell_sector_number=2

mbr=open('mbr','rb')
mbrb=mbr.read(512)
mbr.close()
boot=open('boot','rb')
bootb=boot.read(512)
boot.close()
kernel=open('kernel','rb')
kernelb=kernel.read(512)
kernel.close()
shell=open('shell','rb')
shellb=shell.read(512*shell_sector_number)
shell.close()
img=open('myOS.img','wb')
img.write(mbrb)
img.write(bootb)
img.write(kernelb)
img.write(shellb)
img.close()
